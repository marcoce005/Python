## 2D list == matrici

bevande = ["coca", "fanta", "te", "caffe"]
primi = ["pane", "salame", "formaggio", "pasta"]
dolci = ["torta", "gelato"]

cibi = [bevande, primi, dolci]  ## matrice
print (cibi)

print ("\n\n")

print (cibi[1])     ## stampa la seconda riga 
print (cibi[1] [2])         ## stampa il terzo elemento della seconda riga