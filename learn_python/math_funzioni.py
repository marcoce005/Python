import math

pi = 3.14

x = 1
y = 4
z = 3

print (round(pi))            ## per arrotondare la virgola matematicamente
print (math.ceil(pi))           ## arrotondare per eccesso
print (math.floor(pi))              ## arrotondare per difetto
print (abs(pi))             ## valore assoluto
print (pow(pi, 2))              ## elevamento all'esponente
print (math.sqrt(pi))       ## radice quadrata

print (max(x, y, z))        ## trovare il massimo
print (min(x, y, z))        ## trovare il minimo